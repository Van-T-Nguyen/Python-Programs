def main(): #"This program determines a Hotel bill."
    num_days = int(input("Please input the number of days stayed at the hotel: "))
    cost_per_day = float(input("Please input the cost"
                               " per day to stay at the hotel: "))
    total = num_days*cost_per_day
    print("Your total cost is:", total, "dollars")
    print("Thank you for staying at the Lily Pad hotel")
    print("@..@")
    print("(----)")
    print("( >__< )")
    print("^^  ~~  ^^")
main()